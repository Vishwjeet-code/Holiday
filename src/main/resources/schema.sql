CREATE SEQUENCE IF NOT EXISTS public.project_working_days_seq
START WITH 88001
INCREMENT BY 1
MINVALUE 88001;

-- Create Table
CREATE TABLE IF NOT EXISTS public."projectworkingdays"
(
    project_working_day_id BIGINT PRIMARY KEY
        DEFAULT nextval('public.project_working_days_seq'),

    project_id BIGINT NOT NULL,

    day_id BIGINT NOT NULL,

    working_flag CHAR(1) NOT NULL,

    work_start_time TIME,

    work_end_time TIME,

    created_by VARCHAR(100),

    create_date_time TIMESTAMP,

    updated_by VARCHAR(100),

    update_date_time TIMESTAMP




);

-- Attach sequence ownership AFTER table creation
ALTER SEQUENCE public.project_working_days_seq
OWNED BY public."ProjectWorkingDays".project_working_day_id;

