CREATE SEQUENCE IF NOT EXISTS project_holiday_seq START WITH 770000 INCREMENT BY 1;

CREATE TABLE IF NOT EXISTS projectholiday (
    project_holiday_id BIGINT PRIMARY KEY DEFAULT nextval('project_holiday_seq'),
    project_id BIGINT,
    industry_id BIGINT,
    holiday_id BIGINT,
    city_id BIGINT,
    -- Added this column to match your Spring Boot entity
    created_by VARCHAR(255),
    create_date_time TIMESTAMP,
    updated_by VARCHAR(255),
    update_date_time TIMESTAMP,
    status_id BIGINT
);

CREATE TABLE IF NOT EXISTS holiday (
    holiday_id BIGINT GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    date_id BIGINT,
    country_id BIGINT,
    year_id BIGINT,
    holiday_name VARCHAR(255),
    day VARCHAR(255),
    comment VARCHAR(255),
    created_by VARCHAR(255),
    create_date_time TIMESTAMP,
    updated_by VARCHAR(255),
    update_date_time TIMESTAMP
    );

